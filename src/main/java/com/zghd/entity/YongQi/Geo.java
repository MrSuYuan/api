package com.zghd.entity.YongQi;

/**
 * 位置信息
 */
public class Geo {

    /**
     * 维度
     */
    private float lat;
    /**
     * 经度
     */
    private float lon;

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public float getLon() {
        return lon;
    }

    public void setLon(float lon) {
        this.lon = lon;
    }

}
