package com.zghd.entity.VIVO.response;

/**
 * 图片信息
 */
public class Image {

    /**
     * 地址
     */
    private String url;
    /**
     * 宽
     */
    private int width;
    /**
     * 高
     */
    private int height;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
