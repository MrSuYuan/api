package com.zghd.entity.WangMai;

/**
 * 广告大小
 */
public class AdslotSize {

    /**
     * 广告位宽度
     */
    private int width;
    /**
     * 广告位高度
     */
    private int height;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
