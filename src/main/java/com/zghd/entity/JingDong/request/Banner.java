package com.zghd.entity.JingDong.request;

public class Banner {

    private int w;
    private int h;
    //广告在屏幕位置
    private int pos;

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }
}
