package com.zghd.entity.WangMai;

/**
 * 设备屏幕尺寸
 */
public class ScreenSize {

    /**
     * 设备屏幕宽度
     */
    private int width;
    /**
     * 设备屏幕高度
     */
    private int height;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
