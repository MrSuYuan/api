package com.zghd.entity.HuiChuan;

public class AdPageInfo {
    private String page_url;
    private String page_title;
    private String page_refer;
    private String meta_kw;

    public String getPage_url() {
        return page_url;
    }

    public void setPage_url(String page_url) {
        this.page_url = page_url;
    }

    public String getPage_title() {
        return page_title;
    }

    public void setPage_title(String page_title) {
        this.page_title = page_title;
    }

    public String getPage_refer() {
        return page_refer;
    }

    public void setPage_refer(String page_refer) {
        this.page_refer = page_refer;
    }

    public String getMeta_kw() {
        return meta_kw;
    }

    public void setMeta_kw(String meta_kw) {
        this.meta_kw = meta_kw;
    }
}
